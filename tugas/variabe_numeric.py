# pada Python tidak perlu mendeklarasikan tipe data
w = 3

# Jika anda perlu menentukan suatu tipe data bisa dengan melakukan casting
x = str(3)    # x will be '3'
y = int(3)    # y will be 3
z = float(3)  # z will be 3.0

# Mencetak variabel dengan tipe data number
print(w)
print(x)
print(y)
print(z)